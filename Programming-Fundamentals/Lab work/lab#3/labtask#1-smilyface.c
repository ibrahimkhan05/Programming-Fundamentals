#include <stdio.h>
int main(){
    // multiple printf statements
    printf("\t~ ~\n");
    printf("\t0 0\n");
    printf("\t o\n");
    printf("\t -\n\n");

    // single printf statements
    printf("\t~ ~\n\t0 0\n\t o \n\t - \n\n");

    // multiple printf statements
    printf("\t~ ~\n");
    printf("\t0 0\n");
    printf("\t o \n");
    printf("\t\\_/\n\n");

    //single printf statemets
    printf("\t~ ~\n\t0 0\n\t o \n\t\\_/\n");
    return 0;
}